package com.example.bob.crawlertutorialapp;

public class Sezon {

        private String nr;
        private String link;

    public Sezon(String nr, String link) {
        this.nr = nr;
        this.link = link;
    }

    public String getNr() {
        return nr;
    }

    public String getLink() {
        return link;
    }
}


